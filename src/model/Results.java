package model;

/**
 *
 * @author Dell
 */
public class Results {

    boolean status;
    String message;

    public Results() {
    }

    public Results(boolean status, String message) {
        this.status = status;
        this.message = message;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

}
